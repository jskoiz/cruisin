def write_review_csv(facts: list[dict], path: Path) -> None:
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=REVIEW_FIELDS)
        writer.writeheader()
        for fact in facts:
            row = {field: fact.get(field, "") for field in REVIEW_FIELDS}
            row["tags"] = ", ".join(fact.get("tags", []))
            row["safetyFlags"] = ", ".join(fact.get("safetyFlags", []))
            writer.writerow(row)


