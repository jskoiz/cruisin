CATEGORY_TAGS = {
    "area": ["neighborhood", "local-life"],
    "culture": ["culture", "hawaii"],
    "food": ["food", "local-business"],
    "history": ["history", "heritage"],
    "landmark": ["landmark", "architecture"],
    "lookout": ["viewpoint", "nature"],
    "nature": ["nature", "park"],
    "event": ["event", "time-sensitive"],
}


def canonical_id(name: str, category: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", name.lower()).strip("-")
    prefix = "area" if category == "area" else "poi"
    return f"{prefix}-{slug}"


def confidence_for(source_url: str, source_name: str) -> float:
    source_text = f"{source_name} {source_url}".lower()
    if any(hint in source_text for hint in OFFICIAL_SOURCE_HINTS):
        return 0.95
    if "wikipedia.org" in source_text:
        return 0.78
    return 0.84


def normalize_fact(raw: dict) -> dict:
    category = raw["category"].lower()
    priority = int(raw["priority"])
    source_url = raw["sourceURL"]
    tags = sorted(set(raw.get("tags", []) + CATEGORY_TAGS.get(category, [])))
    importance = min(1.0, max(0.0, priority / 5))
    visual_categories = {"landmark", "lookout", "nature", "area"}
    drive_by_categories = {"area", "landmark", "lookout", "history", "culture"}

    fact = {
        **raw,
        "id": raw.get("id") or canonical_id(raw["name"], category),
        "category": category,
        "subcategory": raw.get("subcategory") or category,
        "tags": tags,
        "sourceURLs": sorted(set(raw.get("sourceURLs", [source_url]))),
        "sourceConfidence": raw.get("sourceConfidence", confidence_for(source_url, raw["sourceName"])),
        "culturalImportance": raw.get("culturalImportance", importance if category == "culture" else min(0.85, importance * 0.7)),
        "historicImportance": raw.get("historicImportance", importance if category == "history" else min(0.85, importance * 0.65)),
        "visualProminence": raw.get("visualProminence", min(1.0, 0.35 + (0.12 * priority)) if category in visual_categories else 0.35),
        "driveByValue": raw.get("driveByValue", min(1.0, 0.40 + (0.12 * priority)) if category in drive_by_categories else 0.45),
        "sensitivity": raw.get("sensitivity", "normal"),
        "safetyFlags": raw.get("safetyFlags", []),
        "evergreen": raw.get("evergreen", True),
        "freshness": raw.get("freshness", "evergreen"),
        "eventStartDate": raw.get("eventStartDate"),
        "eventEndDate": raw.get("eventEndDate"),
        "recurrence": raw.get("recurrence"),
    }
    fact["sourceConfidence"] = round(float(fact["sourceConfidence"]), 2)
    fact["culturalImportance"] = round(float(fact["culturalImportance"]), 2)
    fact["historicImportance"] = round(float(fact["historicImportance"]), 2)
    fact["visualProminence"] = round(float(fact["visualProminence"]), 2)
    fact["driveByValue"] = round(float(fact["driveByValue"]), 2)
    return fact


def dedupe_facts(facts: list[dict]) -> list[dict]:
    seen: set[tuple[str, int, int]] = set()
    result: list[dict] = []
    for fact in facts:
        key = (
            re.sub(r"[^a-z0-9]+", "", fact["name"].lower()),
            round(float(fact["latitude"]) * 10_000),
            round(float(fact["longitude"]) * 10_000),
        )
        if key in seen:
            continue
        seen.add(key)
        result.append(fact)
    return result


